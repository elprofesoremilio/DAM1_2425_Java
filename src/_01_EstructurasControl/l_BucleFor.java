package _01_EstructurasControl;

public class l_BucleFor {
    public static void main(String[] args) {
        // for (inicialización; condición; incremento)
        // Aunque cada sección tiene un nombre, en inicialización y en incremento
        // se puede poner cualquier instrucción de asignación
        // Condición a la fuerza tiene que devolver true o false

        for (int i=0; i<10; i++) {
            System.out.println("Iteración " + i);
        }

    }
}
