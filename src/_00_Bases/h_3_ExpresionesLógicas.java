package _00_Bases;

public class h_3_ExpresionesLógicas {
    public static void main(String[] args) {
        // Operadores lógicos
        // Y AND &&
        boolean resultado = true && true;
        // O OR ||
        resultado = true || false;
        // Negación NO NOT !
        resultado = !resultado;

        resultado = false || !false && true;
        // Orden -->   !     &&     ||
    }
}
